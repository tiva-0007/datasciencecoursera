## `makeCacheMatrix` creates a special "matrix" object that can cache its inverse.
## It is a list of functions that:
## 1.  set the values of the matrix
## 2.  get the values of the matrix
## 3.  set the values of the inverse matrix
## 4.  get the values of the inverse matrix

makeCacheMatrix <- function(x = matrix(sample(1:100, 9), 3, 3)) {
    s <- NULL
    set <- function(y) {
        x <<- y
        s <<- NULL
    }
    get <- function() x
    setsolve <- function(solve) s <<- solve
    getsolve <- function() s
    list(set = set, get = get,
         setsolve = setsolve,
         getsolve = getsolve)
}

## Write a short comment describing this function

## `cacheSolve` computes the inverse of the matrix returned by `makeCacheMatrix` above. 
## If the inverse has already been calculated (and the matrix has not changed), 
## then `cacheSolve` should retrieve the inverse from the cache.

cacheSolve <- function(x, ...) {
    s <- x$getsolve()
    if(!is.null(s)) {
        message('getting inversed matrix')
        return(s)
    }
    data <- x$get()
    s <- solve(data, ...)
    x$setsolve(s)
    s
}

cacheSolve(makeCacheMatrix())